    private void btnGuardarAlumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarAlumActionPerformed
        // TODO add your handling code here:
        boolean lega=true; 
        
        try {
            if(txfNombreAlum.getText().equals("") || txfLegajo.getText().equals("") || txfApellido.getText().equals("")){

                JOptionPane.showMessageDialog(this, "No puede dejar los campos vacios");
            }else{
                for(Alumno a:listAlum){
                    if(a.getLegajo() == Integer.parseInt(txfLegajo.getText())){
                        lega=false;
                        JOptionPane.showMessageDialog(this, "Error no se permiten legajos duplicados");
                        break;
                    }
                }
                listAlum.add(new Alumno(Integer.parseInt(txfLegajo.getText()), txfApellido.getText(), txfNombreAlum.getText()));
                cbxAlumnosForm.addItem(txfApellido.getText() + " " + txfNombreAlum.getText());
                cbxAlumnos.addItem(txfLegajo.getText());
                txfLegajo.setText("");
                txfNombreAlum.setText("");
                txfApellido.setText("");
                JOptionPane.showMessageDialog(this, "Alumno registrado correctamente");
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
            txfLegajo.setText("");
            txfNombreAlum.setText("");
            txfApellido.setText("");
        }
        
    }//GEN-LAST:event_btnGuardarAlumActionPerformed

