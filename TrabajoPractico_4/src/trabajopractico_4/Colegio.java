    private void cbxAlumnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxAlumnosActionPerformed
        // TODO add your handling code here
        
        Alumno alumno = listAlum.get(cbxAlumnos.getSelectedIndex());
        txtNombreCole.setText(alumno.getNombre());
        txtApellidoCole.setText(alumno.getApellido());
        jLabel2.setText(""+alumno.cantidadMateria());
        
        String[] nombreColum ={"Id Materia","Nombre","Año"};
        DefaultTableModel model = new DefaultTableModel(nombreColum, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Todas las celdas no son editables
            }
        };
        tblListaMaterias.setModel(model);
        
        model.setRowCount(0);
        
        for(Materia m:alumno.getListaDeMate()){
            Object[] mate = {
                m.getIdMateria(),
                m.getNombre(),
                m.getAnio()
            };
            model.addRow(mate);
        }
        
    }//GEN-LAST:event_cbxAlumnosActionPerformed

